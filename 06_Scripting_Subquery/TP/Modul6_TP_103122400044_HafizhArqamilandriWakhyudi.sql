SELECT 
    f.JUDUL, 
    f.DURASI, 
    m.NAMA, 
    m.TGL_LAHIR AS TANGGAL_LAHIR_TERMUDA
FROM FILM_103122400044 f
CROSS JOIN MEMBER_103122400044 m
WHERE f.DURASI = (SELECT MIN(DURASI) FROM FILM_103122400044)
AND m.TGL_LAHIR = (SELECT MAX(TGL_LAHIR) FROM MEMBER_103122400044);


SELECT 
    t.ID_THEATER, 
    t.KELAS, 
    COUNT(tr.KODE_PEMESANAN) AS TOTAL_PEMAKAIAN
FROM THEATER_103122400044 t
JOIN JADWAL_103122400044 j ON t.ID_THEATER = j.ID_THEATER
JOIN TRANSAKSI_103122400044 tr ON j.ID_JADWAL = tr.ID_JADWAL
GROUP BY t.ID_THEATER, t.KELAS
HAVING COUNT(tr.KODE_PEMESANAN) = (
    SELECT MAX(COUNT(tr2.KODE_PEMESANAN))
    FROM JADWAL_103122400044 j2
    JOIN TRANSAKSI_103122400044 tr2 ON j2.ID_JADWAL = tr2.ID_JADWAL
    GROUP BY j2.ID_THEATER
);