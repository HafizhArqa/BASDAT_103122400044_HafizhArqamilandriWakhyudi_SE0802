SELECT 
    m.NAMA, 
    (EXTRACT(YEAR FROM SYSDATE) - EXTRACT(YEAR FROM m.TGL_LAHIR)) AS UMUR
FROM MEMBER_103122400044 m
JOIN TRANSAKSI_103122400044 t ON m.ID_MEMBER = t.ID_MEMBER
JOIN JADWAL_103122400044 j ON t.ID_JADWAL = j.ID_JADWAL
JOIN FILM_103122400044 f ON j.ID_FILM = f.ID_FILM
WHERE f.JUDUL = 'Habibie Ainun 3'
AND j.ID_THEATER IN (
    SELECT ID_THEATER 
    FROM THEATER_103122400044 
    WHERE HARGA > (SELECT HARGA FROM THEATER_103122400044 WHERE ID_THEATER = 'Teater 1')
);

SELECT 
    f.JUDUL, 
    th.ID_THEATER, 
    th.HARGA
FROM FILM_103122400044 f
JOIN JADWAL_103122400044 j ON f.ID_FILM = j.ID_FILM
JOIN THEATER_103122400044 th ON j.ID_THEATER = th.ID_THEATER
WHERE th.HARGA = (SELECT MIN(HARGA) FROM THEATER_103122400044)
OR th.HARGA = (SELECT MAX(HARGA) FROM THEATER_103122400044)
ORDER BY th.HARGA;



SELECT f.JUDUL, COUNT(j.ID_JADWAL) AS JUMLAH_TAYANG
FROM FILM_103122400044 f
JOIN JADWAL_103122400044 j ON f.ID_FILM = j.ID_FILM
GROUP BY f.JUDUL
HAVING COUNT(j.ID_JADWAL) > (
    SELECT MIN(COUNT(ID_JADWAL))
    FROM JADWAL_103122400044
    GROUP BY ID_FILM
);

SELECT 
    j.ID_THEATER, 
    f.JUDUL, 
    f.DURASI
FROM JADWAL_103122400044 j
JOIN FILM_103122400044 f ON j.ID_FILM = f.ID_FILM
WHERE f.DURASI > 100
GROUP BY j.ID_THEATER, f.JUDUL, f.DURASI
HAVING COUNT(j.ID_JADWAL) > (
    SELECT AVG(COUNT(ID_JADWAL)) 
    FROM JADWAL_103122400044 
    GROUP BY ID_FILM
);

SELECT 
    m.NAMA, 
    m.NO_HP, 
    f.JUDUL
FROM MEMBER_103122400044 m
JOIN TRANSAKSI_103122400044 t ON m.ID_MEMBER = t.ID_MEMBER
JOIN JADWAL_103122400044 j ON t.ID_JADWAL = j.ID_JADWAL
JOIN FILM_103122400044 f ON j.ID_FILM = f.ID_FILM
WHERE m.NO_HP LIKE '081%'
AND j.ID_THEATER IN (
    SELECT ID_THEATER 
    FROM THEATER_103122400044 
    WHERE HARGA > 30000
);