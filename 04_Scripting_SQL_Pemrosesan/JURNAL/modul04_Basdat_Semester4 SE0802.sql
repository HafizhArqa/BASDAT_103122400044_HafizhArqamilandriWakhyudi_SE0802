SELECT 
    f.JUDUL, 
    j.ID_THEATER, 
    j.PERIODE_START, 
    (j.PERIODE_END - j.PERIODE_START) AS LAMA_WAKTU_HARI
FROM jadwal_103122400044 j
JOIN film_103122400044 f ON j.ID_FILM = f.ID_FILM
WHERE f.JUDUL = 'Habibie Ainun 3';


--

SELECT 
    t.ID_THEATER, 
    f.JUDUL, 
    t.KELAS, 
    t.HARGA,
    CASE 
        WHEN t.HARGA < 50000 THEN 'Murah'
        WHEN t.HARGA > 75000 THEN 'Mahal'
        ELSE 'Normal'
    END AS KATEGORI
FROM jadwal_103122400044 j
JOIN film_103122400044 f ON j.ID_FILM = f.ID_FILM
JOIN theater_103122400044 t ON j.ID_THEATER = t.ID_THEATER;

--

SELECT 
    m.ID_MEMBER, 
    TO_NUMBER(t.HARGA) AS HARGA, 
    (2019 - EXTRACT(YEAR FROM TO_DATE(m.TGL_LAHIR, 'DD-MM-YYYY'))) AS UMUR,
    CASE 
        WHEN (2019 - EXTRACT(YEAR FROM TO_DATE(m.TGL_LAHIR, 'DD-MM-YYYY'))) <= 32 
        THEN TO_NUMBER(t.HARGA) * 0.9
        ELSE TO_NUMBER(t.HARGA)
    END AS SALE
FROM transaksi_103122400044 tr
JOIN member_103122400044 m ON tr.ID_MEMBER = m.ID_MEMBER
JOIN jadwal_103122400044 j ON tr.ID_JADWAL = j.ID_JADWAL
JOIN theater_103122400044 t ON j.ID_THEATER = t.ID_THEATER
ORDER BY UMUR;

--

SELECT 
    m.ID_MEMBER, 
    m.NAMA, 
    f.JUDUL
FROM transaksi_103122400044 tr
JOIN member_103122400044 m ON tr.ID_MEMBER = m.ID_MEMBER
JOIN jadwal_103122400044 j ON tr.ID_JADWAL = j.ID_JADWAL
JOIN film_103122400044 f ON j.ID_FILM = f.ID_FILM
WHERE f.DURASI >= 100 
  AND TO_NUMBER(SUBSTR(m.NO_HP, -1)) = 8;
  
--

SELECT 
    tr.ID_MEMBER, 
    f.ID_FILM, 
    j.ID_THEATER, 
    t.HARGA AS HARGA_ASLI,
    CASE 
        WHEN f.JUDUL = 'Keluarga Cemara' AND j.PERIODE_START >= TO_DATE('2019-12-19', 'YYYY-MM-DD') 
        THEN t.HARGA * 0.3
        ELSE t.HARGA
    END AS SALE
FROM transaksi_103122400044 tr
JOIN jadwal_103122400044 j ON tr.ID_JADWAL = j.ID_JADWAL
JOIN film_103122400044 f ON j.ID_FILM = f.ID_FILM
JOIN theater_103122400044 t ON j.ID_THEATER = t.ID_THEATER;